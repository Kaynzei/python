#tuples are used to store multiple items in a single variable. the difference with lists is that we cant change the value of tuple later
three_numbers = (1, 2, 3)
print(three_numbers)
#tuples allows repetition of thesame value
three_numbers = (1, 2, 3, 1)
print(three_numbers)
#we can also check the length using len print(len(three_numbers))
#tuple allow various kinds of variable
strings = ('home', 'land', 'earth')
print(strings)
#it allows the mixture of various data types
three_numbers2 = (1, 'home', 3, 4, 'land')
print(three_numbers2)
